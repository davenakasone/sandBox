#ifndef H_StackADT
#define H_StackADT

//----------------------------------------------------------------------------------------------------------------------------
//============================================================================================================================
//----------------------------------------------------------------------------------------------------------------------------
template <class T> 
class stackADT
{
    public:
        virtual void initializeStack() = 0;           // initialize the stack to an empty state
        virtual bool isEmptyStack() const = 0;        // determine if stack is empty
        virtual bool isFullStack() const = 0;         // determine if the stack is full
        virtual void push(const T& newItem) = 0;   // add newItem to the stack...must exist and have space
        virtual T top() const = 0;                 // return the top element of the stack....without moving anything
        virtual void pop() = 0;                       // remove the top element of the stack
};


//----------------------------------------------------------------------------------------------------------------------------
//============================================================================================================================
//----------------------------------------------------------------------------------------------------------------------------    
#endif // stackADT.h
